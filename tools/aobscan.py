import sys, struct, re

def load_image(path):
    raw = open(path, 'rb').read()
    e_lfanew = struct.unpack_from('<I', raw, 0x3C)[0]
    assert raw[e_lfanew:e_lfanew+4] == b'PE\x00\x00'
    coff = e_lfanew + 4
    num_sections = struct.unpack_from('<H', raw, coff+2)[0]
    size_opt = struct.unpack_from('<H', raw, coff+16)[0]
    opt = coff + 20
    size_of_image = struct.unpack_from('<I', raw, opt+56)[0]
    size_of_headers = struct.unpack_from('<I', raw, opt+60)[0]
    img = bytearray(size_of_image)
    img[:size_of_headers] = raw[:size_of_headers]
    sect = opt + size_opt
    for i in range(num_sections):
        off = sect + i*40
        vsize = struct.unpack_from('<I', raw, off+8)[0]
        vaddr = struct.unpack_from('<I', raw, off+12)[0]
        rsize = struct.unpack_from('<I', raw, off+16)[0]
        raddr = struct.unpack_from('<I', raw, off+20)[0]
        n = min(rsize, vsize if vsize else rsize)
        img[vaddr:vaddr+n] = raw[raddr:raddr+n]
    return bytes(img)

def sig_to_re(sig):
    parts = []
    for tok in sig.split():
        if tok.startswith('?'):
            parts.append(b'.')
        else:
            parts.append(re.escape(bytes([int(tok, 16)])))
    return re.compile(b''.join(parts), re.DOTALL)

def scan(img, sig, limit=20):
    rx = sig_to_re(sig)
    return [m.start() for m in list(rx.finditer(img))[:limit]]

